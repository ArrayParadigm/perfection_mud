package perfection;

public class MudRoom {
    private int number;
    private String name;
    private String description;

    public MudRoom(int number, String name, String description) {
        this.number = number;
        this.name = name;
        this.description = description;
    }

    public int getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }
}
